def drop_unused_columns(df: pd.DataFrame) -> pd.DataFrame:
    df = df.copy()

    existing_cols_to_drop = [col for col in COLS_TO_DROP if col in df.columns]
    missing_cols = [col for col in COLS_TO_DROP if col not in df.columns]

